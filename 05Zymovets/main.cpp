#include <iostream>
#include "AComplex.h"
#include "TComplex.h"
using std::cout;
using std::endl;
//************************************************************
// Реалізувати два варіанти класу комплексних чисел :
//1) AComplex комплексні числа в алгебричній формі;
//
//2) TComplex комплексні числа в тригонометричній формі.
//
//Адитивні операції визначити і реалізувати в алгебричній 
// формі, мультипликативні - в тригонометричній.
//
//Виконати тестування реалізованих класів, відслідковуючи 
//створення об'єктів і перетворення типів зокрема при 
//застосуванні арифметичних операцій до виразів змішаних 
//типів, використовуючи аргументами операцій дійсні числа, 
//комплексні числа в алгебричній  або тригонометричній формі, 
//Developed by Ruslan Zymovets (IPZ, group 1)
//on February 21
//Version 1.0
//************************************************************

int main(void)
{
	AComplex a1(1, 1), a2(0, 1), a(a1);
	TComplex t1(1, 3.14159), t2(1, 0), t(a1);
	cout << "<> - AComplex, {} - TComplex" << endl;
	cout << "#AComplex" << endl;
	cout << "a1 = " << a1 << endl;
	cout << "a2 = " << a2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 + a2 = " << a1 + a2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 - a2 = " << a1 - a2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 * a2 = " << a1 * a2 << " (TComplex result!)" << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 / a2 = " << a1 / a2 << " (TComplex result!)" << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "#TComplex" << endl;
	cout << "t1 = " << t1 << endl;
	cout << "t2 = " << t2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 + t2 = " << t1 + t2 << " (AComplex result!)" << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 - t2 = " << t1 - t2 << " (AComplex result!)" << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 * t2 = " << t1 * t2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 / t2 = " << t1 / t2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "#Both AComplex & TComlex" << endl;
	cout << "a = " << a << endl;
	cout << "t = " << t << endl;
	cout << "a + t = " << a + t << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t + a = " << t + a << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a - t = " << a - t << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t - a = " << t - a << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a * t = " << a * t << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t * a = " << t * a << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a / t = " << a / t << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t / a = " << t / a << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "#Operations with assigning" << endl;
	cout << "a1 = " << a1 << endl;
	cout << "a2 = " << a2 << endl;
	cout << "t1 = " << t1 << endl;
	cout << "t2 = " << t2 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 += a1 = " << (a1 += a1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 += t1 = " << (a1 += t1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 += a1 = " << (t1 += a1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 *= t1 = " << (t1 *= t1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t2 *= a2 = " << (t2 *= a2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a2 *= t2 = " << (a2 *= t2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "-=  and  /=  are analogical to previous two." << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "#When real numbers get complex" << endl;
	a1.re() = 3; a1.im() = 4;
	cout << "a1 = " << a1 << endl;
	cout << "t1 = " << t1 << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 + 2 = " << (a1 + 2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "2 + a1 = " << (2 + a1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "a1 * 2 = " << (a1 * 2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "2 * a1 = " << (2 * a1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 + 2 = " << (t1 + 2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "2 + t1 = " << (2 + t1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "t1 * 2 = " << (t1 * 2) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	cout << "2 * t1 = " << (2 * t1) << endl;
	cout << "---------------------------------------------------------------------------------------------" << endl;
	return 0;
} 